package Pack01;

public class UserData {
	String name;
	String pin;
	String cand;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPin() {
		return pin;
	}
	public void setPin(String pin) {
		this.pin = pin;
	}
	public String getCand() {
		return cand;
	}
	public void setCand(String cand) {
		this.cand = cand;
	}
	
}
